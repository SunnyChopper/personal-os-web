/** Normalize trailing slashes so route checks match `/admin/login` and `/admin/login/`. */
export function isAdminLoginPath(pathname: string): boolean {
  const p = pathname.replace(/\/+$/, '') || '/';
  return p === ROUTES.admin.login;
}

